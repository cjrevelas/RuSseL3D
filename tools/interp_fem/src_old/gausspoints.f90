subroutine gausspoints(ll, lint, s)
!--------------------------------------------------------------------------------------------------------------------------------------------!
implicit none
!--------------------------------------------------------------------------------------------------------------------------------------------!
integer, intent(in)  :: ll
integer, intent(out) :: lint
integer              :: i, j

real(8), intent(out), dimension(5,*) :: s
!--------------------------------------------------------------------------------------------------------------------------------------------!
if (ll==3) then
    lint = 11
endif

do i = 1,3
   do j = 1,10
      s(i,j) = 0.0d0
   enddo

   s(i,i)   = 1.00d0
   s(i,i+4) = 0.50d0
   s(i,i+7) = 0.50d0
   s(i,11)  = 0.25d0
enddo

s(2,5)  = 0.50d0
s(3,6)  = 0.50d0
s(1,10) = 0.50d0

do j = 1,4
   s(5,j) = 1.d0/360.d0
enddo

do j = 5,10
   s(5,j) = 1.d0/90.d0
enddo

s(5,11) = 4.d0/45.d0

s(1,7)  = 0.5d0
s(1,10) = 0.0d0

!compute fourth points
do j = 1, lint
    s(4,j) = 1.d0 - (s(1,j) + s(2,j) + s(3,j))
enddo
!--------------------------------------------------------------------------------------------------------------------------------------------!
end subroutine gausspoints
